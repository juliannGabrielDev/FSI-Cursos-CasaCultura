    private void TConsultasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TConsultasMousePressed
        int fila = TConsultas.getSelectedRow();
        if (fila >= 0) {
            String nombre = objToString(TConsultas.getValueAt(fila, 1));
            String cedula = objToString(TConsultas.getValueAt(fila, 2));
            String especialidad = objToString(TConsultas.getValueAt(fila, 3));
            String telefono = objToString(TConsultas.getValueAt(fila, 4));
            String correo = objToString(TConsultas.getValueAt(fila, 5));

            TNombre.setText(nombre);
            TCedula.setText(cedula);
            TEspecialidad.setText(especialidad);
            TTelefono.setText(telefono);
            TCorreo.setText(correo);
        }
    }//GEN-LAST:event_TConsultasMousePressed

