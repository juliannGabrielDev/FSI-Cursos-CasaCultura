    private void BActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BActualizarActionPerformed
        int fila = TConsultas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un instructor para editar.");
            return;
        }

        String id = TConsultas.getValueAt(fila, 0).toString();

        String[] datos = {
            id,
            TNombre.getText(),
            TCedula.getText(),
            TEspecialidad.getText(),
            TTelefono.getText(),
            TCorreo.getText()
        };

        if (cnx.actualizar(tabla, datos) == 1) {
            JOptionPane.showMessageDialog(this, "Instructor actualizado");
            cargarDatos();
            limpiarCampos();
            generarGrafica();
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar.");
        }
    }//GEN-LAST:event_BActualizarActionPerformed

    private void BEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEliminarActionPerformed
        int fila = TConsultas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un instructor para eliminar.");
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "¿Eliminar este instructor?", "Confirmar", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            String id = TConsultas.getValueAt(fila, 0).toString();
            // El método borrar necesita un array donde la pos 0 sea el ID
            String[] datos = {id};

            if (cnx.borrar(tabla, datos) == 1) {
                JOptionPane.showMessageDialog(this, "Instructor eliminado");
                cargarDatos();
                limpiarCampos();
                generarGrafica();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar.");
            }
        }
    }//GEN-LAST:event_BEliminarActionPerformed

    private void BPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPDFActionPerformed
        String query = "SELECT i.nombre_completo AS Instructor, c.nombre_curso AS Curso, "
                + "a.aula AS Aula, a.horario_especifico AS Horario "
                + "FROM instructores i "
                + "INNER JOIN asignaciones a ON i.id_instructor = a.id_instructor "
                + "INNER JOIN cursos c ON a.id_curso = c.id_curso "
                + "ORDER BY i.nombre_completo";

        float[] anchos = new float[]{3f, 3f, 2f, 2f};

        // Título del Doc, Encabezado Tabla, Query, Anchos, Nombre Archivo (sin .pdf)
        int estado = cnx.crearPDF("Casa de la Cultura", "Reporte de Cursos Asignados", query, anchos, "ReporteInstructores");

        if (estado == 1) {
            cnx.visualizarPDF("ReporteInstructores");
        } else {
            JOptionPane.showMessageDialog(this, "Error al generar PDF.");
        }
    }//GEN-LAST:event_BPDFActionPerformed

