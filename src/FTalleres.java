    private void BAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAgregarActionPerformed
        if (DCFechaInicio.getDate() == null || DCFechaFin.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Por favor selecciona las fechas de inicio y fin.");
            return;
        }

        String[] datos = {
            "", // ID autoincrementable
            TNombre.getText(),
            TDescripcion.getText(),
            SCosto.getValue().toString(),
            CBNivel.getSelectedItem().toString(),
            cnx.toSQL(DCFechaInicio.getDate()),
            cnx.toSQL(DCFechaFin.getDate())
        };

        if (cnx.insertar(tabla, datos) == 1) {
            JOptionPane.showMessageDialog(this, "Taller agregado exitosamente.");
            cargarDatos();
            limpiarCampos();
            generarGrafica();
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar el taller.");
        }
    }//GEN-LAST:event_BAgregarActionPerformed

    private void BActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BActualizarActionPerformed
        int fila = TConsultas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un taller de la tabla para actualizar.");
            return;
        }

        if (DCFechaInicio.getDate() == null || DCFechaFin.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Las fechas no pueden estar vacías.");
            return;
        }

        String id = TConsultas.getValueAt(fila, 0).toString();

        String[] datos = {
            id,
            TNombre.getText(),
            TDescripcion.getText(),
            SCosto.getValue().toString(),
            CBNivel.getSelectedItem().toString(),
            cnx.toSQL(DCFechaInicio.getDate()),
            cnx.toSQL(DCFechaFin.getDate())
        };

        if (cnx.actualizar(tabla, datos) == 1) {
            JOptionPane.showMessageDialog(this, "Taller actualizado exitosamente.");
            cargarDatos();
            limpiarCampos();
            generarGrafica();
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar.");
        }
    }//GEN-LAST:event_BActualizarActionPerformed

    private void BEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEliminarActionPerformed
        int fila = TConsultas.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un taller para eliminar.");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(this,
                "¿Estás seguro de eliminar este taller?\nSe borrarán también sus asignaciones.",
                "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.YES_OPTION) {
            String id = TConsultas.getValueAt(fila, 0).toString();
            String[] datos = {id};

            if (cnx.borrar(tabla, datos) == 1) {
                JOptionPane.showMessageDialog(this, "Taller eliminado.");
                cargarDatos();
                limpiarCampos();
                generarGrafica();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar.");
            }
        }
    }//GEN-LAST:event_BEliminarActionPerformed

    private void BPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPDFActionPerformed
        String query = "SELECT nombre_curso AS Taller, nivel AS Nivel, "
                + "CONCAT('$', costo_semestral) AS Costo, "
                + "fecha_inicio AS Inicio FROM cursos ORDER BY nivel, nombre_curso";

        float[] anchos = new float[]{4f, 2f, 2f, 2f};

        int estado = cnx.crearPDF("Casa de la Cultura", "Catálogo de Talleres Disponibles", query, anchos, "ReporteTalleres");

        if (estado == 1) {
            cnx.visualizarPDF("ReporteTalleres");
        } else {
            JOptionPane.showMessageDialog(this, "Error al generar PDF.");
        }
    }//GEN-LAST:event_BPDFActionPerformed

