    private void TConsultasMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TConsultasMousePressed
        int fila = TConsultas.getSelectedRow();

        if (fila >= 0) {
            String nombre = objToString(TConsultas.getValueAt(fila, 1));
            String descripcion = objToString(TConsultas.getValueAt(fila, 2));
            String costoStr = objToString(TConsultas.getValueAt(fila, 3));
            String nivel = objToString(TConsultas.getValueAt(fila, 4));
            String fInicioStr = objToString(TConsultas.getValueAt(fila, 5));
            String fFinStr = objToString(TConsultas.getValueAt(fila, 6));

            TNombre.setText(nombre);
            TDescripcion.setText(descripcion);
            CBNivel.setSelectedItem(nivel);

            try {
                double costo = Double.parseDouble(costoStr);
                SCosto.setValue(costo);
            } catch (NumberFormatException e) {
                System.out.println("Error al convertir costo: " + e.getMessage());
                SCosto.setValue(0.0);
            }

            DCFechaInicio.setDate(cnx.toDate(fInicioStr));
            DCFechaFin.setDate(cnx.toDate(fFinStr));
        }
    }//GEN-LAST:event_TConsultasMousePressed

